package java_basics;

public class Variables {

	public static void main(String[] args) {
		// Variable in programming is where you can save a value
		
		int x = 4;
		
		System.out.println(x);
		
		x = 11;
		
		System.out.println(x * 10);
		
		System.out.println(x);
		
		String words;
		words = "This is a sentance.";
		
		// (String words = "This is a sentance")  works the same way
		
		System.out.println(words);
		
		System.out.println(words + " These are more words. ");
	}

}
